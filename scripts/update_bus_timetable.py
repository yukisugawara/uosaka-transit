def main() -> None:
    path = DATA_DIR / "timetables.json"
    with open(path, encoding="utf-8") as f:
        data = json.load(f)

    data["shuttle_bus"] = {
        "fare": 0,
        "weekday_only": True,
        "revision": "2025-04-01",
        "routes": REAL_SHUTTLE_ROUTES,
    }

    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    # サマリー表示
    total = 0
    for r in REAL_SHUTTLE_ROUTES:
        n = len(r["timetable"])
        total += n
        print(f"  {r['route_name']:30s}  {n:2d} 便  （約{r['duration_min']}分）")
    print(f"\n  合計 {total} 便 / {len(REAL_SHUTTLE_ROUTES)} 路線")
    print(f"  保存先: {path}")


if __name__ == "__main__":
    main()
